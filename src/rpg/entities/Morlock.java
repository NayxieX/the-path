package rpg.entities;
import rpg.enums.ReputationLevel;
public class Morlock extends Enemy {

    public Morlock() {
        super("Морлок", 100);
    }

    @Override
    public String getDescription() {
        return "Морлок: " + getName() + " ХП: " + getHp();
    }

    // Монолог Морлока перед діалоговим боєм. Залежить від репутації героя.
    @Override
    public String getReaction(ReputationLevel heroLevel) {
        return switch (heroLevel) {
            case HERO -> """
                    "Ти допомагав людям які не могли віддячити."
                    "Захищав тих кого навіть не знав."
                    "Я теж колись був таким."
                    "Саме тому я знаю що вони зроблять з тобою врешті решт."
                    "Вибач що не можу дозволити тобі пройти через те що пройшов я."
                    Він намагається вбити тебе - щоб врятувати від своєї долі.""";
            case NEUTRAL -> """
                    "Ти не добрий і не злий. Просто... людина."
                    "Знаєш - я заздрю тобі."
                    "Я ніколи не міг бути просто людиною."
                    "А потім вони забрали єдине заради чого я все це робив."
                    "Тепер ти зрозумів чому я тут стою?"
                    Він питає, чи ти розумієш. Справжнє питання без відповіді.""";
            case ANTAGONIST -> """
                    Морлок дивиться і... сміється. Вперше за сотні років.
                    "Ти брав від слабких. Зраджував тих хто вірив."
                    "І після всього прийшов зупинити мене?"
                    "Ми з тобою однакові."
                    "Різниця лише в тому що я хоча б чесний щодо того ким є."
                    Він бачить себе в тобі. Його єдина радість.""";
        };
    }
}
